    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String xCoordinate = jTextField1.getText().trim();
        String yCoordinate = jTextField2.getText().trim();
        String length = jTextField3.getText().trim();
        String width = jTextField4.getText().trim();



      if (xCoordinate.isEmpty() || yCoordinate.isEmpty() || length.isEmpty() || width.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Some fields are empty. Please fill in all fields.", "Input Error", JOptionPane.ERROR_MESSAGE);
           return;
        }
      
       
     
     
        if (!xCoordinate.matches("\\d+") || !yCoordinate.matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "Coordinates must be integers.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!length.matches("\\d+(\\.\\d+)?") || !width.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(this, "Length and Width must be numbers.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int x = Integer.parseInt(xCoordinate);
        int y = Integer.parseInt(yCoordinate);
        double len = Double.parseDouble(length);
        double wid = Double.parseDouble(width);

        Rectangle rectangle = new Rectangle();
        rectangle.setPosition(new Point(x, y));
        
        Map<String, Double> properties = new HashMap<>();
        properties.put("length", len);
        properties.put("width", wid);
        rectangle.setProperties(properties);

        drawing.addShape(rectangle);
        drawing.refresh(MainWindow.g);
        jComboBox.addItem("Rectangle" + drawing.getShapes().size());
        dispose();
    
            
        

       


      
        
    }//GEN-LAST:event_jButton1ActionPerformed

 
